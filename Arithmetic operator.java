import java.util.*;
class Arithmetic
{
   public static void main(String[]args)
   {
      Scanner sc = new Scanner(System.in);
      System.out.println("enter a,b values");
      int a = sc.nextInt();
      int b = sc.nextInt();
      System.out.println(a+b);
      System.out.println(a-b);
      System.out.println(a*b);
      System.out.println(a/b);
      System.out.println(a%b);
   }
}
